# Escape characters

# sometimes we need to print special unprintable characters such as ''""(){}[]
#for these case we use \ to escape those characters

# \t to print tab
#example
print("Hello World")
print("Hello\tWorld")
print("-------------")

# \n to print in the next line(new line)
print("Hello World")
print("Hello\nWorld")
print("-------------")

# \\ to print \ itself
print("Hello World")
print("Hello\\World")
print("-------------")

# \" prints double quote " character
print("Hello World")
print("Hello\"World")




