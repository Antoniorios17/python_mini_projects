# print() function
# used to display information to users
# it accepts zero or more expressions as arguments separated by commas

my_name = "Peter"
my_age = 23

#print using % formatter
print("Hello my name is %s and I am %s years old" %(my_name, my_age))

#print using the format method

print("Hello My name is {} and I am {} years old".format(my_name, my_age))
